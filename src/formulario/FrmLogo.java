    private void imagem1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_imagem1MouseClicked
        // TODO add your handling code here:
        if (evt.getClickCount() >= 2) {
            try {
                JFileChooser file = new JFileChooser();
                FileNameExtensionFilter filtro = new FileNameExtensionFilter("", "jpg", "png");
                file.addChoosableFileFilter(filtro);
                file.setAcceptAllFileFilterUsed(false);
                file.setDialogType(JFileChooser.OPEN_DIALOG);
                //file.setCurrentDirectory(new File("/"));
                file.showOpenDialog(this);

                String caminhaImagem = file.getSelectedFile().getPath();

                ImageIcon icon = new ImageIcon(Utilidade.ManipulaImagem.setImagemDimensao(caminhaImagem, 700, 300));

                this.imagem1.setIcon(icon);
                this.imagem1.setText("");
            } catch (Exception e) {

            }
        }
    }//GEN-LAST:event_imagem1MouseClicked


    //CARREGA GRELHA OTIMIZADA-------------------------------------------------
    public final void carregaGrelha() {

        int contador = 0;
        DefaultTableModel modelo = (DefaultTableModel) grelha.getModel();
        modelo.setNumRows(0);
        List<Chip> lista = null;

        if (radChip.isSelected()) {

            lista = chipDao.getListagemLikePorChipOuCelular(txtPesquisa.getText().trim(), "buscarPorChip");
        } else {
            lista = chipDao.getListagemLikePorChipOuCelular(txtPesquisa.getText().trim(), "celular");
        }
        lista = chipDao.getListagemLike(txtPesquisa.getText().trim());
        modelo.setNumRows(0);
        for (Chip chip : lista) {

            String sim = chip.isIsDado() == true ? "Sim" : "";
            String nao = chip.isIsTelefonia() == true ? "Sim" : "";

            modelo.addRow(new Object[]{
                chip.getIdChip(),
                chip.getCodigoChip(),
                chip.getNumeroLinha(),
                sim,
                nao,
                chip.getEmpresa().getNomeEmpresa(),
                chip.getStatus(),});

            contador++;
        }
        lblQuantidade.setText("Quantidade: " + contador);
        
        pintaInativos();
    }
    
        // PINTANDO GRADE 
    private void pintaInativos() {
        grelha.setDefaultRenderer(Object.class, new DefaultTableCellRenderer() {
            public Component getTableCellRendererComponent(JTable table, Object value, boolean isSelected, boolean hasFocus, int row, int column) {
                JLabel label = (JLabel) super.getTableCellRendererComponent(table, value, isSelected, hasFocus, row, column);

                //*******************************************
                int coluna = 6;

                String procurado = (String) grelha.getValueAt(row, coluna);

                if (procurado.equalsIgnoreCase("EMPRESTADO")) {
                    label.setForeground(Color.BLUE);
                } else {
                    label.setForeground(Color.BLACK);
                }

                //*******************************************
                return label;

            }
        });
    }
    
    
    
    
    
    
    
    
    
    
    
    

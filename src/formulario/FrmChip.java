    private void grelhaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_grelhaMouseClicked
        // TODO add your handling code here:
        try {
            if (grelha.getRowCount() > 0) {

                int codigo = (int) grelha.getValueAt(grelha.getSelectedRow(), 0);
                Chip chip = chipDao.getPorID(codigo);

                txtCodigo.setText(chip.getIdChip() + "");
                txtChip.setText(chip.getCodigoChip());
                txtTelefone.setText("");
                txtTelefone.setText(chip.getNumeroLinha());
                txtObservacao.setText(chip.getObservacao());
                cboSituacao.setSelectedItem(chip.getStatus());
                cboOperadora.getModel().setSelectedItem(chip.getEmpresa());

                if (chip.isIsTelefonia()) {
                    ckVoz.setSelected(true);
                } else {
                    ckVoz.setSelected(false);
                }

                if (chip.isIsDado()) {
                    ckDados.setSelected(true);
                } else {
                    ckDados.setSelected(false);
                }

                btnAlterar.setEnabled(true);
                btnExcluir.setEnabled(true);
                novo = false;

                txtVisao.setText(verificaComQuemEsta(chip.getIdChip()));
            }

        } catch (Exception e) {
        }
    }//GEN-LAST:event_grelhaMouseClicked

    // FUNÇÃO BUSCANDO COM QUEM ESTÁ O CHIP //////////////////////////////////
    private String verificaComQuemEsta(int id) {
        String resultado = "";
        Emprestimo emprestimo = new EmprestimoDao().buscaPorChipEmprestado(id);
        if (emprestimo != null) {
            resultado = "Emprestado para: " + emprestimo.getFuncionario().getLocalidade().getNomeLocalidade() + " - Funcionario(a): " + emprestimo.getFuncionario().getNome()
                    + " - Data: " + emprestimo.getDataEmprestimo();

        }

        return resultado;
    }



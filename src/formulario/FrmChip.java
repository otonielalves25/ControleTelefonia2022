    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        // TODO add your handling code here:

        if (txtChip.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Codigo do chip não informada", null, JOptionPane.ERROR_MESSAGE);
            txtChip.requestFocus();
            return;
        }
        if (txtTelefone.getText().replace("(", "").replace(")", "").replace("-", "").length() < 0) {
            JOptionPane.showMessageDialog(this, "Telefone inválido.", null, JOptionPane.ERROR_MESSAGE);
            txtChip.requestFocus();
            return;

        }
        if (cboSituacao.getSelectedItem().equals("Selecione...")) {
            JOptionPane.showMessageDialog(this, "Situação não informada.", null, JOptionPane.ERROR_MESSAGE);
            cboSituacao.requestFocus();
            return;
        }

        if (cboOperadora.getSelectedItem().equals("Selecione...")) {
            JOptionPane.showMessageDialog(this, "Operadora não informada.", null, JOptionPane.ERROR_MESSAGE);
            cboOperadora.requestFocus();
            return;
        }

        // CRIA A CLASSE MARCA MODELO //////////////////////////////////////////
        Chip chip = new Chip();
        chip.setCodigoChip(txtChip.getText().trim());
        chip.setNumeroLinha(txtTelefone.getText());
        chip.setObservacao(txtObservacao.getText());
        chip.setStatus(cboSituacao.getSelectedItem().toString());
        Empresa empresa = (Empresa) cboOperadora.getSelectedItem();
        chip.setEmpresa(empresa);

        if (ckDados.isSelected()) {
            chip.setIsDado(true);
        } else {
            chip.setIsDado(false);
        }
        if (ckVoz.isSelected()) {
            chip.setIsTelefonia(true);
        } else {
            chip.setIsTelefonia(false);
        }

        // CADATRAO NOVO NO BANCO //////////////////////////////////////////////
        if (novo) {
            Chip xip = chipDao.retornaPorChip(txtChip.getText());
            Chip linha = chipDao.retornaPorNumeroLinha(txtChip.getText());
            // se o chip já tem cadastro
            if (xip != null) {
                limparPouco();
                txtChip.requestFocus();
                JOptionPane.showMessageDialog(this, "Chip já tem cadastro", null, JOptionPane.ERROR_MESSAGE);

                return;
            }
            // se a linha tem cadastro
            if (linha != null) {
                limparPouco();
                txtChip.requestFocus();
                JOptionPane.showMessageDialog(this, "Número da linha já tem cadastro", null, JOptionPane.ERROR_MESSAGE);
                return;
            }
            chipDao.insert(chip);
            JOptionPane.showMessageDialog(this, "Cadatrado com Sucesso !!!", null, JOptionPane.INFORMATION_MESSAGE);
            // ALTERAR CADASTRO NO BANCO ///////////////////////////////////////
            logDao.insert("Cadatrado chip: " + txtChip.getText().trim());
        } else {

            chip.setIdChip(Integer.parseInt(txtCodigo.getText()));
            chipDao.update(chip);
            JOptionPane.showMessageDialog(this, "Alterada com Sucesso !!!", null, JOptionPane.INFORMATION_MESSAGE);
            logDao.insert("Alterada chip: " + txtChip.getText().trim());
        }

        // VERIFICA QUE SÃO VARIOS IGUAIS //////////////////////////////////////
        if (ckVarios.isSelected()) {
            botaoNovo();
            limparPouco();
            novo = true;
            habilitado(true);
            cboSituacao.setEnabled(false);
        } else {
            limparTudo();
            botaoInicial();
            novo = false;
            habilitado(false);
        }

        carregaGrelha();

    }//GEN-LAST:event_btnSalvarActionPerformed


     private void funcaoBarraProgresso() {

        new Thread() {
            public void run() {

                for (int i = 0; i <= 100; i++) {
                    try {
                        sleep(8);  // velocidade que a barra anda

                        progressoBar.setValue(i);

                        switch (progressoBar.getValue()) {
                            case 10:
                                lblMensagem.setText("10%");
                                //sleep(100); // velocidade que a barra anda
                                break;
                            case 30:
                                lblMensagem.setText("30%");
                                //sleep(100); // velocidade que a barra anda
                                break;
                            case 40:
                                lblMensagem.setText("40%");
                                //sleep(100); // velocidade que a barra anda
                                break;
                            case 50:
                                lblMensagem.setText("50%");
                                //sleep(100); // velocidade que a barra anda
                                break;
                            case 70:
                                lblMensagem.setText("70%");
                                //sleep(100); // velocidade que a barra anda
                                break;
                            case 80:
                                lblMensagem.setText("80%");
                                //sleep(100); // velocidade que a barra anda
                                break;
                            case 90:
                                lblMensagem.setText("90%");
                                //sleep(100); // velocidade que a barra anda
                                break;
                            case 95:
                                lblMensagem.setText("95%");
                                //sleep(1000); // dá uma parada na barra
                                break;
                            case 100:
                                dispose();
                                break;
                            default:
                                break;
                        }

                    } catch (InterruptedException ex) {
                        Logger.getLogger(FrmBarraProgresso.class.getName()).log(Level.SEVERE, null, ex);
                    }

                } // fim do for	
            } // fim run
        }.start(); // fim thread

    }


    private void entrarSistema() {

        //----------------------------------------------------------------------
        // Validando campos preenchidos
        if ("".equals(txtUsuario.getText())) {
            JOptionPane.showMessageDialog(this, "Usuário não informado", "Erro", JOptionPane.ERROR_MESSAGE);
            txtUsuario.requestFocus();
            return;
        }

        if ("".equals(txtSenha.getText())) {
            JOptionPane.showMessageDialog(this, "Senha não informada", "Erro", JOptionPane.ERROR_MESSAGE);
            txtSenha.requestFocus();
            return;
        }

        if ("".equals(txtConfirmaSenha.getText())) {
            JOptionPane.showMessageDialog(this, "Confirmar Senha não informada", "Erro", JOptionPane.ERROR_MESSAGE);
            txtConfirmaSenha.requestFocus();
            return;
        }

        if (!txtConfirmaSenha.getText().equalsIgnoreCase(txtSenha.getText())) {
            JOptionPane.showMessageDialog(this, "As senha não são iguais.", "Erro", JOptionPane.ERROR_MESSAGE);
            txtConfirmaSenha.requestFocus();
            return;
        }
        //----------------------------------------------------------------------
        // PEGA OS DADOS DO LOGADO /////////////////////////////////////////////
        int idUsuario = Session.getIdUsuario();
        String senha = ConvertendoSenhaMD5.ConvertendoSenha(txtSenha.getText());

        //Convertendo senha para md5 no banco
        UsuarioDao dao = new UsuarioDao();

        if (dao.trocarSenha(senha, idUsuario)) {
            JOptionPane.showMessageDialog(this, "Senha alterada com sucesso", "Sucesso", JOptionPane.INFORMATION_MESSAGE);
            // PEGANDO LOGS DO SISTEMA 
            logDao.insert("Senha alterada: " + txtUsuario.getText());
            this.dispose();
        } else {
            JOptionPane.showMessageDialog(this, "Erro ao alterar a senha.", "Erro", JOptionPane.ERROR_MESSAGE);
            txtSenha.setText("");
            txtSenha.requestFocus();

        }
    }


